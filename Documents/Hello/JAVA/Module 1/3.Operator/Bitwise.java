class Bitwise  //. AND OR XOR 
{
	public static void main(String[] args)
	{
		int a=11;
		int b=13;

		System.out.println(a&b);
		System.out.println(a|b);
		System.out.println(a^b);
		System.out.println();


		int x=21;
		int y=25;

		System.out.println(x&y);
		System.out.println(x|y);
		System.out.println(x^y);
		System.out.println();

		int p=27;
		int q=45;

		System.out.println(p&q);
		System.out.println(p|q);
		System.out.println(p^q);
	}
}