class Program4
{
	public static void main(String[] args)
	{
		double radius = 4.5;
		double pi= 3.14;
		double area = pi*radius*radius;
		double circumference = 2*pi*radius;

		System.out.println("Area of circle : " +area);
		System.out.println("Circumference of Circle : " +circumference);
		System.out.println();
	}
}