class Square
{
	public static void main(String[] args)
	{
		int n=5; //Size of Square

		for(int i=0; i<n; i++)   // No. of Rows
		{
			for(int j=0; j<n; j++)  // No. of Columns
			{
				System.out.print("*"+" ");
			}
			System.out.println(); // Move to next line
		}
	}
}

